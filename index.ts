/********************
 * TYPE ANNOTATIONS *
 ********************/

let num:number = 45;
let str:string = "Hello World";
console.log(`My number is ${num}`)
console.log(`My string is ${str}`);

/******************************
 * DYNAMIC TYPE DETERMINATION *
 ******************************/

let greeting = "Hello";
console.log(greeting);

/*******************************
 * ANY DATA TYPE *
 ******************************/

let variable:any = 54;
variable = "John";
console.log(variable);